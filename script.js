let gallery = document.querySelector(".gallery");
let lightbox = document.querySelector(".lightbox");
let bigimg = document.querySelector(".big-img");

let closeBtn = document.querySelector(".close");
let prevBtn = document.querySelector(".prev");
let nextBtn = document.querySelector(".next");

let buttons = document.querySelectorAll(".buttons button");
let loadBtn = document.querySelector("#loadBtn");

let currentImages = images;
let currentIndex = 0;

let visible = 8;



function showImages(arr){

gallery.innerHTML = "";

currentImages = arr;

arr.slice(0,visible).forEach(function(image,index){

let card = document.createElement("div");
card.classList.add("card");

card.innerHTML = `

<img src="${image.pic}">
<h3>${image.title || image.category}</h3>

`;

card.addEventListener("click",function(){

currentIndex = index;

bigimg.src = image.pic;

lightbox.style.display="flex";

});

gallery.appendChild(card);

});



if(visible >= arr.length){

loadBtn.style.display="none";

}else{

loadBtn.style.display="block";

}

}



showImages(images);





function filterImages(category){

visible = 8;



buttons.forEach(btn=>btn.classList.remove("active"));

event.target.classList.add("active");



if(category==="all"){

showImages(images);

return;

}



let filtered = images.filter(function(image){

return image.category===category;

});



showImages(filtered);

}





loadBtn.addEventListener("click",function(){

visible +=4;

showImages(currentImages);

});





nextBtn.addEventListener("click",function(){

currentIndex++;

if(currentIndex>=currentImages.length){

currentIndex=0;

}

bigimg.src=currentImages[currentIndex].pic;

});





prevBtn.addEventListener("click",function(){

currentIndex--;

if(currentIndex<0){

currentIndex=currentImages.length-1;

}

bigimg.src=currentImages[currentIndex].pic;

});





closeBtn.addEventListener("click",function(){

lightbox.style.display="none";

});





lightbox.addEventListener("click",function(e){

if(e.target===lightbox){

lightbox.style.display="none";

}

});





document.addEventListener("keydown",function(e){

if(lightbox.style.display==="flex"){

if(e.key==="ArrowRight"){

nextBtn.click();

}

if(e.key==="ArrowLeft"){

prevBtn.click();

}

if(e.key==="Escape"){

lightbox.style.display="none";

}

}

});





